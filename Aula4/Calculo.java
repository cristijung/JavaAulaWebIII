
public class Calculo {
    public static void main(String args[]) {
        //declaracão dos atributos - variáveis
        //observe que, qdo as variáveis são do mesmo tipo, posso declarar na mesma linha
        
        double x, y;
        //atribuicão de valores às variáveis
        x = 40;
        y = 32.8;
        
        System.out.println("A soma de x + y = " + (x + y));
        System.out.println("A divisão de x + y = " + (x / y));
    }
}